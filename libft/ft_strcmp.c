int	ft_strcmp(const char *s1, const char *s2)
{
	unsigned char	s11;
	unsigned char	s22;
	int				i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i])
		i++;
	s11 = s1[i];
	s22 = s2[i];
	return (s11 - s22);
}
